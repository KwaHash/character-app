export function calculateNakshatra(birthDate: Date): Nakshatra | null {
  try {
    // Get year, month, day
    const year = birthDate.getFullYear()
    const month = birthDate.getMonth()
    const day = birthDate.getDate()
    
    // Use noon as default time for consistency
    const hours = 12
    const minutes = 0
    
    // Calculate approximate lunar phase (0-29.5)
    const lunarAge = calculateApproximateLunarAge(year, month, day, hours, minutes)
    
    // Convert lunar age to moon longitude (0-360 degrees)
    const moonLongitude = (lunarAge / 29.5) * 360
    
    // Get Nakshatra using the moon longitude
    return getNakshatraByMoonDegree(moonLongitude)
  } catch (error) {
    console.error('ナクシャトラの計算中にエラーが発生しました:', error)
    return null
  }
}

// Helper function to calculate approximate lunar age
function calculateApproximateLunarAge(year: number, month: number, day: number, hours: number, minutes: number): number {
  // Julian date calculation (approximate)
  const a = Math.floor((14 - (month + 1)) / 12)
  const y = year + 4800 - a
  const m = (month + 1) + 12 * a - 3
  
  let jd = day + Math.floor((153 * m + 2) / 5) + 365 * y + Math.floor(y / 4) - Math.floor(y / 100) + Math.floor(y / 400) - 32045
  
  // Add time of day
  jd += (hours - 12) / 24 + minutes / 1440
  
  // New moon epoch (approximate)
  const newMoonEpoch = 2451550.09766 // January 6, 2000 18:14 UTC
  const lunarPeriod = 29.530588853 // days
  
  // Calculate lunar age
  const lunarCycles = (jd - newMoonEpoch) / lunarPeriod
  const lunarAge = (lunarCycles - Math.floor(lunarCycles)) * lunarPeriod
  
  return lunarAge
}

// Improved version of getNakshatraByMoonDegree
export function getNakshatraByMoonDegree(moonLongitude: number): Nakshatra {
  // Each Nakshatra spans 13°20' (13.33333 degrees)
  const nakshatraSpan = 13.33333
  
  // Normalize the longitude to 0-360 range
  const normalizedLongitude = moonLongitude % 360
  
  // Calculate Nakshatra index (0-26)
  const nakshatraIndex = Math.floor(normalizedLongitude / nakshatraSpan)
  
  // Return the corresponding Nakshatra
  return nakshatras[nakshatraIndex]
}

// より正確なナクシャトラ計算のためのヘルパー関数
export function calculateCompatibility(nakshatra1: Nakshatra, nakshatra2: Nakshatra): {
  score: number;
  compatibility: string;
  description: string;
} {
  // 相性スコアの初期値（0〜100）
  let score = 50
  
  // 互換性のあるナクシャトラリストに含まれているかチェック
  if (nakshatra1.compatibleNakshatras.includes(nakshatra2.id)) {
    score += 20
  }
  
  // 同じガナ（神性、人間、魔性）の場合
  if (nakshatra1.gana === nakshatra2.gana) {
    score += 10
  }
  
  // 同じ元素の場合
  if (nakshatra1.element === nakshatra2.element) {
    score += 10
  }
  
  // 支配惑星の相性
  const planetaryCompatibility = {
    '太陽': ['木星', '火星'],
    '月': ['水星', '金星'],
    '火星': ['太陽', '月', '木星'],
    '水星': ['金星', '土星'],
    '木星': ['太陽', '月', '火星'],
    '金星': ['水星', '土星'],
    '土星': ['水星', '金星'],
    'ラーフ': ['金星', '土星'],
    'ケートゥ': ['火星', '木星']
  }
  
  if (planetaryCompatibility[nakshatra1.ruling_planet as keyof typeof planetaryCompatibility]?.includes(nakshatra2.ruling_planet) ||
      planetaryCompatibility[nakshatra2.ruling_planet as keyof typeof planetaryCompatibility]?.includes(nakshatra1.ruling_planet)) {
    score += 10
  }
  
  // 相性の評価
  let compatibility = ''
  let description = ''
  
  if (score >= 90) {
    compatibility = '最高の相性'
    description = '非常に調和的で深い結びつきがある関係です。互いの長所を高め合い、精神的にも物質的にも成長できるでしょう。'
  } else if (score >= 75) {
    compatibility = 'とても良い相性'
    description = '互いに良い影響を与え合う調和的な関係です。共通の目標に向かって協力し合うことができます。'
  } else if (score >= 60) {
    compatibility = '良い相性'
    description = '基本的に調和的ですが、時に小さな摩擦が生じることもあります。コミュニケーションを大切にすることで、関係は成長します。'
  } else if (score >= 40) {
    compatibility = '普通の相性'
    description = '互いに学ぶべき点が多い関係です。違いを尊重し、理解を深めることで、関係は改善されるでしょう。'
  } else if (score >= 25) {
    compatibility = 'やや難しい相性'
    description = '互いの違いが大きく、調和を保つには努力が必要です。忍耐と理解を持って接することが重要です。'
  } else {
    compatibility = '困難な相性'
    description = '根本的な価値観や性格の違いが大きい関係です。互いを変えようとするのではなく、違いを受け入れることが必要です。'
  }
  
  return {
    score,
    compatibility,
    description
  }
}

